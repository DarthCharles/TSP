    private void panelMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_panelMouseClicked

        Mapa.mouseclicked(evt);
        panel.repaint();
        Mapa.ruta.clear();
        if (recorridox) {
            dialog.setVisible(false);
        }

        Mapa.costo = 0;
        ventana_recorrido = true;
        dibujar_ruta = false;
        // TODO add your handling code here:
    }//GEN-LAST:event_panelMouseClicked

    private void ruta_mas_cortaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ruta_mas_cortaActionPerformed
        if (ventana_recorrido) {

            Mapa.rutacorta();
            Mapa.costo = 0;
            Mapa.get_costo();
            panel.repaint();
            recorridox = true;
            dibujar_ruta = true;
            dialog = new Recorrido(new javax.swing.JFrame(), false);
            dialog.addWindowListener(new java.awt.event.WindowAdapter() {

                @Override
                public void windowClosing(java.awt.event.WindowEvent e) {
                    Mapa_TSP.ventana_recorrido = true;
                    Mapa.ruta.clear();
                    dialog.setVisible(false);
                }
            });
            dialog.setVisible(true);
        } else {
            JOptionPane.showMessageDialog(rootPane, "Ya ha calculado la ruta mas corta");
        }
        ventana_recorrido = false;
      
        // TODO add your handling code here:
    }//GEN-LAST:event_ruta_mas_cortaActionPerformed

