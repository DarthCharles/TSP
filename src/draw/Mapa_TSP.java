    private void deshacerActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deshacerActionPerformed
        if (!Mapa.Cuidades.isEmpty()) {
            int pos = Mapa.Cuidades.size() - 1;
            Mapa.Estados.get(Mapa.Cuidades.get(pos).getPosición()).contador--;
            Mapa.Cuidades.remove(Mapa.Cuidades.size() - 1);
            Mapa.ruta.clear();
            panel.repaint();
            if (recorridox) {
                dialog.setVisible(false);
            }
        } else {
            JOptionPane.showMessageDialog(rootPane, "Nada que deshacer");
        }

// TODO add your handling code here:
    }//GEN-LAST:event_deshacerActionPerformed

